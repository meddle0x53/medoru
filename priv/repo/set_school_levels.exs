IO.puts("Loaded #{map_size(school_levels)} kanji school level mappings")

# Count by grade
for grade <- 1..3 do
  count = Enum.count(school_levels, fn {_k, v} -> v == grade end)
  IO.puts("  Grade #{grade}: #{count} kanji")
end
IO.puts("  (Note: Complete data has 1026 kanji for grades 1-6)")

# Update kanji with school levels
IO.puts("\nUpdating kanji in database...")

updated =
  Repo.transaction(fn ->
    school_levels
    |> Enum.reduce(0, fn {character, level}, acc ->
      case Repo.get_by(Kanji, character: character) do
        nil ->
          # Silent skip - kanji not in database
          acc

        kanji ->
          kanji
          |> Ecto.Changeset.change(school_level: level)
          |> Repo.update!()

          acc + 1
      end
    end)
  end)

IO.puts("\nUpdated #{updated} kanji with school levels")

# Show distribution in database
IO.puts("\nDistribution in database:")

for level <- 1..6 do
  count =
    from(k in Kanji, where: k.school_level == ^level)
    |> Repo.aggregate(:count, :id)

  label = if level == 7, do: "SL#{level} (Jr. High)", else: "SL#{level}"
  IO.puts("  #{label}: #{count} kanji")
end

# Show kanji without school level
other_count =
  from(k in Kanji, where: is_nil(k.school_level))
  |> Repo.aggregate(:count, :id)

IO.puts("  No SL: #{other_count} kanji")

